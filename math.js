function sum(a, b)
{
    return a + b;
}

function substract(a, b)
{
    return a - b;
}

function multiply(a, b)
{
    return a * b;
}

function division(a, b)
{
    return a / b;
}

function divisionWithRemain(a, b)
{
    return a % b;
}

function squareRoot(a, b)
{
    return Math.sqrt(a, b);
}

function power(a, b)
{
    return Math.pow(a, b);
}

